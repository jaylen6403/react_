export const getConversionRate = (
  productCurrency: string,
  selectedCurrency: string,
  conversionData: CurrencyMap
): number => {
  if (productCurrency === selectedCurrency) {
    return 1;
  }
  const data = conversionData[productCurrency];

  if (data) {
    const value = data[selectedCurrency];
    if (value) return value.rate;
  }

  return 1;
};

export const notional = (
  data: PortfolioV2[],
  selectedCurrency: string,
  currencyConversionData: Currency[]
): PortfolioSums => {
  const { portfolioProducts } = data[0];

  if (!portfolioProducts) {
    return portfolioDefault;
  }

  const currencyConversionMap: CurrencyMap = currencyConversionData.reduce(
    (acc, cur) => {
      if (cur.conversionRates) {
        acc[cur.isoCode] = cur.conversionRates;
      }

      return acc;
    },
    {} as CurrencyMap
  );

  return portfolioProducts.reduce((acc, cur) => {
    const { currency } = cur;
    const conversionRate = getConversionRate(
      currency || '',
      selectedCurrency,
      currencyConversionMap
    );

    const growthSum = cur.orders?.reduce((preVal, currVal) => {
      if (currVal.quantity) {
        return preVal + currVal?.quantity;
      }
      return preVal;
    }, 0);

    const notionalSum = growthSum ? growthSum * conversionRate : 0;
    const count = cur.orders?.length || 0;

    if (cur.returnType === 'Growth') {
      const totalGrowthCount = acc.growth.count + count;
      const totalGrowthNotional = acc.growth.notionalSum + notionalSum;
      return {
        ...acc,
        growth: { count: totalGrowthCount, notionalSum: totalGrowthNotional },
      };
    }

    if (cur.returnType === 'Income') {
      const totalIncomeCount = acc.income.count + count;
      const totalIncomeNotional = acc.income.notionalSum + notionalSum;
      return {
        ...acc,
        income: { count: totalIncomeCount, notionalSum: totalIncomeNotional },
      };
    }

    if (cur.returnType === 'Growth & Income') {
      const totalGAndICount = acc['growth & income'].count + count;
      const totalGAndINotional =
        acc['growth & income'].notionalSum + notionalSum;
      return {
        ...acc,
        'growth & income': {
          count: totalGAndICount,
          notionalSum: totalGAndINotional,
        },
      };
    }

    if (cur.returnType === 'Digital') {
      const totalDigitalCount = acc.digital.count + count;
      const totalDigitalNotional = acc.digital.notionalSum + notionalSum;
      return {
        ...acc,
        digital: {
          count: totalDigitalCount,
          notionalSum: totalDigitalNotional,
        },
      };
    }
    return acc;
  }, portfolioDefault);
};


type PortfolioSum = {
  count: number;
  notionalSum: number;
};

type PortfolioSums = {
  growth: PortfolioSum;
  income: PortfolioSum;
  'growth & income': PortfolioSum;
  digital: PortfolioSum;
};

type CurrencyMap = {
  [key: string]: { [key: string]: ConversionRate };
};

